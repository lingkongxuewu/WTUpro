#define		FASTER			0		//内部采用快速算法，完成通讯数据的CRC运算，占用较大程序空间
#define		MIDDLE			1		//在快速算法与慢速算法之间的折衷，速度及占用的程序空间处于两者之间
#define		SLOW			2		//慢速算法，但占用的程序空间最少。
